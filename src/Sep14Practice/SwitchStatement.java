package Sep14Practice;

public class SwitchStatement {

    public static void main(String[] args) {


        int i = 100;

        switch (i) {

            case 100 :
                System.out.println("A");
                break;
            case 1020:
                System.out.println('B');
                break;

            case 90 :
                System.out.println("Case 90");
                break;
        }

    }
}
