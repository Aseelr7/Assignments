        System.out.println("* * * * * *  * * * * * *  * * * * * *");
        int result2 = (5 + 3) * 2;
        System.out.println(result2);

        System.out.println("* * * * * *  * * * * * *  * * * * * *");
        int result3 = 10 - 4 + 2 * 3;
        System.out.println(result3);

        System.out.println("* * * * * *  * * * * * *  * * * * * *");
        int result4 = 8 + 12 / 4 * 2;
        System.out.println(result4);

